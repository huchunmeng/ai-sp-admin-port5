// 愤怒状态后备词库（5梯队递进）
export const ANGRY_FALLBACKS = [
  // 第1梯队：口头反击
  '你什么态度？！', '你嘴里放干净点！', '你再说一遍试试？',
  '你骂谁呢？！', '你凭什么这么跟我说话？',
  // 第2梯队：质疑+威胁
  '你到底会不会看病？', '有你这么当医生的吗？', '我不跟你说，叫你们主任来！',
  '你再这样我投诉你！', '我看你是根本没把我当人看。',
  // 第3梯队：拒绝沟通
  '我不想跟你说话了。', '你别问了，我不说了。', '你问别人去吧，别问我。',
  '你爱怎么着怎么着吧。', '我没什么好跟你说的了。',
  // 第4梯队：去人格化（动作/沉默）
  '（狠狠瞪着你，一言不发）', '（扭过头去不看你）',
  '（站起来收拾东西准备走）', '（胸口起伏，气得说不出话）',
  // 第5梯队：终极沉默
  '哼……', '嗯'
]

// 冷漠/不悦状态后备词库
export const COLD_FALLBACKS = [
  '你这样让我很不舒服。', '你态度能不能好一点？', '我干嘛要回答你？',
  '你这么凶干什么？', '我不说了。', '你问别人去吧。',
  '你好好说话行不行？', '你到底是什么态度？', '有你这么问话的吗？',
  '（别过脸去不看你）', '嗯', '我不想说了。'
]

// 悲伤状态后备词库
export const SAD_FALLBACKS = [
  '我现在很难受……', '你让我安静一下好吗……', '你根本不理解我……',
  '我真的好痛苦……', '别问了……我受不了了……',
  '（低着头不说话，眼泪在眼眶里打转）', '（默默流泪，用手背擦眼睛）',
  '你走吧，我什么也不想说了……'
]

// 暴怒状态后备词库（1-5字极短回怼）
export const FURIOUS_FALLBACKS = [
  '不关你事。', '少来问我。', '不知道。', '滚！',
  '我不跟你说。', '你管不着。', '走开。', '别烦我。',
  '哼。', '（怒视不吭声）', '（转过身去不理你）'
]

// 中性状态后备词库（默认池）
export const NEUTRAL_FALLBACKS = [
  '您问吧。', '嗯，您说。', '您继续问吧。',
  '医生您问具体点。', '我不太会说，您问吧。',
  '您想知道什么就问。', '我听着呢。',
]

/**
 * 根据引擎状态选择合适的后备词库
 * @param {string} state - 引擎状态
 * @param {object} vector - 情绪向量 {anger, fear, sadness, joy}
 * @returns {string[]}
 */
export function selectFallbackPool(state, vector = {}) {
  if (state === 'furious') return FURIOUS_FALLBACKS
  if (state === 'angry' || vector.anger >= 7) return ANGRY_FALLBACKS
  if (vector.anger >= 4) return COLD_FALLBACKS
  if (vector.sadness >= 4) return SAD_FALLBACKS
  return NEUTRAL_FALLBACKS // calm/uneasy/怀疑 等默认中性
}
