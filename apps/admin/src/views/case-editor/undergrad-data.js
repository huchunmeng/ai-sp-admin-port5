// 获取某科室的症状列表
export function getUndergradSymptoms(specialty) {
  const entry = undergradSymptomMap[specialty]
  return entry ? entry.symptoms : []
}

// 获取某科室+症状的病种列表（无 mastery 过滤，全部适用）
export function getUndergradDiseases(specialty, symptom) {
  const entry = undergradSymptomMap[specialty]
  if (!entry) return []
  return (entry.symptomMap || {})[symptom] || []
}

// 汇总所有症状（去重）
export const undergradSymptomList = (() => {
  const seen = new Set()
  const result = []
  for (const entry of Object.values(undergradSymptomMap)) {
    for (const s of entry.symptoms) {
      if (!seen.has(s)) { seen.add(s); result.push(s) }
    }
  }
  return result.sort()
})()
